void	ft_rev_int_tab(int *tab, int size)
{
	int buff;
	int i;
	int j;

	i = 0;
	j = size - 1;
	while (i < j)
	{
		buff = *(tab + i);
		*(tab + i) = *(tab + j);
		*(tab + j) = buff;
		i++;
		j--;
	}
}
